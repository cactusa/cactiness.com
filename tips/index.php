                <h1>How to grow a cactus</h1>

                <p>With their incredible variety of body shapes, spination, and beautiful flowers, cacti are intriguing. Cacti make perfect potted specimens for a bright windowsill position, or outdoors. They are the perfect accompaniment to other succulents for landscaping, creating a dramatic and unique style.</p>

                <p>Many people find that what starts as a small cactus collection on a windowsill soon turns into a lifelong hobby, or dare we say &helip; obsession!<br/>
                The myriad forms and varieties make it easy to become 'hooked on cacti'.</p>

                <h2>Cacti Cultivation Guide</h2>

                <p>Cacti come from diverse habitats and hence have a range of requirements, but can be cultivated successfully by following a few general guidelines, as follows:</p>

                <h3 id="sunicon">Light</h3>

                <p>Most cacti require a bright position with at least some direct sun, but will do best in a very sunny position.<br/>
                Cacti can be grown in a shadier position, for example, a windowsill which receives reduced light. In this case plants require much less fertiliser and much less water than when grown in a sunnier position.<br/>
                Some cacti that are better suited to a low light position: Mamillaria "Arizona Snowcap", Gymnocalycium species (G. baldianum, G. mihanovichii), Aporocactus species (Rats' Tail Cactus), Rhipsalis species.<br/>
                It should be noted that plants grown shaded might not flower.</p>

                <h3 id="tempicon">Temperature</h3>

                <p>Cacti can handle a full range of temperatures from below freezing, up to around 50°C.<br/>
                The extremes require careful handling, cacti preferring a range of 10-30°C.<br/>
                In very hot conditions, shading and ventilation is required. In very cold conditions cacti should be kept dry.<br/>
                In the garden, where plants can't be kept dry, adequate air flow around plants and good drainage is important.</p>

                <h3 id="dropsicon">Watering</h3>

                <p>Cacti generally require generous watering during their growing season, which is, for most cacti, the warmer months.<br/>
                During the cold months cacti can be kept dry for the 3-6 months of winter. Some cacti require a light sprinkling of water during this period and will continue to grow slowly (e.g. many columnar cacti and epiphytes). Other cacti go quite dormant and will shrivel significantly and should be given no water. (e.g. Echinocereus).</p>

                <p>When cacti are watered, during the growing period, they require a good soaking of the growing medium, as is the case for all potted plants.<br/>
                Between waterings soil should become almost dry.(This allows the roots to breathe). This 'almost dry' state of the soil can be identified by the plant body beginning to shrink, or become soft or wrinkles showing on the skin. A well watered plant is firm and the skin is often shiny. In contrast, an under watered plant is soft to the touch, shrivelled in appearance, and the skin dull in tone.</p>

                <p>Watering can be done by overhead sprinkling, i.e. automatic watering sprinklers, hand hosing, or using a watering can. Bottom watering from flood trays or saucers, where water is soaked up through the soil from the base of the pot, is also fine.<br/>
                Whichever way you water, soak the soil thoroughly.</p>

                <h3>Soil, Re-potting and Fertilising</h3>

                <p>Cacti in habitat grow in all kinds of soils. The most important thing to know is that these soils all drain well, and cacti from all over the Americas will grow in any potting medium, in cultivation, as long as it is open and free draining.<br/>
                For example, a good cactus mix is any basic potting mix with an extra 1/3 to ½ coarse sharp sand added. Or, alternatively; 1/3 composted pine bark (e.g. basic potting mix), 1/3 sandy loam, and 1/3 coarse, sharp sand. Many variations to this are also feasible.</p>

                <p>Methods of fertilising your cactus can be varied:</p>
                <ul>
                    <li>Slow release fertilisers added to the potting medium.</li>
                    <li>Liquid fertilisers applied through watering during the growing season. (Of course, when plants aren't growing water and fertilisers won't be absorbed).</li>
                    <li>Top dressing annually with organic fertilisers such as manure or blood and bone. (This is particularly appropriate for a garden situation).</li>
                </ul>

                <p>Many people think that cacti don't need fertiliser, but many plants are heavy feeders. Look for signs of plant yellowing, slower than expected growth, or if you know a plant hasn't been fertilised for 1-2 years, it is either time to fertilise or re-pot it.</p>

                <p>Since most cacti require re-potting every 1-2 years, we recommend fertilising is done when plants are re-potted. This is as simple as mixing a slow release fertiliser into the potting medium.</p>

                <p>When re-potting a cactus you can either pot it on or re-pot it.<br/>
                Potting on is usually done with younger plants or rapidly growing plants that have outgrown their pots. Re-potting is appropriate for full grown, older plants or plants that have become diseased, or even plants that refuse to grow for whatever reason.<br/>
                Potting on involves removal of the old pot without disturbing the root ball, placing it in a larger pot and simply filling up with new soil.<br/>
                Re-potting involves removal of most or all of the soil from the roots and replacing the plant in the same sized pot, or larger if needed.</p>

