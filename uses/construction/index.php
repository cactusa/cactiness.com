                <p>Cacti are used as construction materials. Living cactus fences are employed as barricades. Such fences are inexpensive to develop in regions where cacti are common, and can provide an extreme deterrent to any but a determined human intruder. Often their primary function is to keep wandering large animals off a private property. The woody parts of cacti such as Cereus repandus and Echinopsis atacamensis are used in buildings and in furniture. The frames of wattle and daub houses built by the Seri people of Mexico may use parts of Carnegiea gigantea. The very fine spines and hairs (trichomes) of some cacti were used as a source of fibre for filling pillows and in weaving.</p>
