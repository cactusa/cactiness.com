                <p>Mexican laws state that tequila can be produced only in the state of Jalisco and limited regions in the states of Guanajuato, Michoacán, Nayarit, and Tamaulipas. Mexico has claimed the exclusive international right to the word "tequila", threatening legal actions against manufacturers of distilled blue agave spirits in other countries,Tequila’ can only be produced in Mexico.</p>

                <p>Tequila is most often made at a 38–40% alcohol content (76–80 proof), but can be produced between 31–55% alcohol content (70–110 proof).</p>

