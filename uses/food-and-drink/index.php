				<p>Almost any cactus fruit which is fleshy is edible. The word pitaya or pitahaya (usually considered to have been taken into Spanish from Haitian creole) can be applied to a range of "scaly fruit", particularly those of columnar cacti. The fruit of the saguaro (Carnegiea gigantea) has long been important to the indigenous peoples of northwestern Mexico and the southwestern United States, including the Sonoran Desert. It can be preserved by boiling to produce syrup and by drying. The syrup can also be fermented to produce an alcoholic drink. Fruits of Stenocereus species have also been important food sources in similar parts of North America; Stenocereus queretaroensis is cultivated for its fruit. In more tropical southern areas, the climber Hylocereus undatus provides pitahaya orejona, now widely grown in Asia under the name "dragon fruit". Other cacti providing edible fruit include species of Echinocereus, Ferocactus, Mammillaria, Myrtillocactus, Pachycereus, Peniocereus and Selenicereus. The bodies of cacti other than opuntias are less often eaten, although Anderson reports that Neowerdermannia vorwerkii is prepared and eaten like potatoes in upland Bolivia.</p>

