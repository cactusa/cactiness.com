                <p>Tequila (Spanish pronunciation: [teˈkila]) is a liquor distilled beverage made from the blue agave plant, primarily in the area surrounding the city of Tequila, 65 kilometres (40 mi) northwest of Guadalajara, and in the highlands (Los Altos) of the western Mexican state of Jalisco.</p>

                <p>The red volcanic soil in the surrounding region is particularly well suited to the growing of the blue agave, and more than 300 million of the plants are harvested there each year. Agave tequila grows differently depending on the region. Blue agaves grown in the highlands region are larger in size and sweeter in aroma and taste. Agaves harvested in the lowlands, on the other hand, have a more herbaceous fragrance and flavor.</p>

