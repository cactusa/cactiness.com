                <p>The plant now known as Opuntia ficus-indica or the Indian fig cactus has long been an important source of food. The original species is thought to have come from central Mexico, although this is now obscure because the indigenous people of southern North America developed and distributed a range of horticultural varieties (cultivars), including forms of the species and hybrids with other opuntias. Both the fruit and pads are eaten, the former often under the Spanish name tuna, the latter under the name nopal. Cultivated forms are often significantly less spiny or even spineless. The nopal industry in Mexico was said to be worth US$150 million in 2007. The Indian fig cactus was probably already present in the Caribbean when the Spanish arrived, and was soon after brought to Europe. It spread rapidly in the Mediterranean area, both naturally and by being introduced – so much so that early botanists assumed that it was native to the area. Outside the Americas, the Indian fig cactus is an important commercial crop in Sicily, Algeria and other north African countries. Fruits of other opuntias are also eaten, generally under the same name, tuna. Flower buds, particularly of Cylindropuntia species, are also consumed.</p>
