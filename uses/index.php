                <h1>Uses for cacti and succulents</h1>

                <h2>All succulents have a very wide range of uses:</h2>
                
                <p>A cactus may be used for decoration, as the main colorful, vegetative form in a desert environment.</p>
                <p>It may be used for drink, such as the famous tequila.</p>
                <p>It may be used for employment, such as the crew of pollinators that make cactus fruits and seeds possible from cactus flowers.</p>
                <p>It may be used for environmental health, through the carbon dioxide that it takes in and the oxygen that it releases through its stomata, or breathing pores.</p>
                <p>It may be used for people and wildlife finding their way, through its landmark forms in the monotonous desert environment. It may be used for food, such as the berries and vegetables that the prickly pear cactus [Opuntia spp] offers.</p>
                <p>It may be used for making money, such as through the dyes that are produced by cochineal insects [Dactylopius coccus] on the prickly pear cactus [Opuntia spp].</p>
                <p>It may be used for shelter, such as the nesting cavities that the giant saguaro cactus [Carnegiea gigantea] offers to the Gila woodpecker [Melanerpes uropygialis].</p>
                <p>It may be used for soil and water conservation, through its soil holding and water intaking roots.</p>
                <p>It may be used for tools, such as the living fences that people make from any columnar cactus plant.</p>
