                <p>Cacti were cultivated as ornamental plants from the time they were first brought to the New World. By the early 1800s enthusiasts in Europe had large collections (often including other succulents as well as cacti). Rare plants were sold for very high prices. Suppliers of cacti and other succulents employed collectors to obtain plants from the wild, in addition to growing their own. In the late 1800s, collectors turned to orchids, and cacti became less popular, although never disappearing from cultivation.</p>

                <p>Cacti are often grown in greenhouses, particularly in regions unsuited to the cultivation of cacti outdoors, such the northern parts of Europe and North America. Here they may be kept in pots or grown in the ground. Cacti are also grown as houseplants, many being tolerant of the often dry atmosphere. Cacti in pots may be placed outside in the summer to ornament gardens or patios, and then kept under cover during the winter. Less drought resistant epiphytes such as epiphyllum hybrids, Schlumbergera (the Thanksgiving or Christmas cactus) and Hatiora (the Easter cactus) are widely cultivated as houseplants.</p>

                <p>Cacti may also be planted outdoors in regions with suitable climates. Concern for water conservation in arid regions has led to the promotion of gardens that require less watering (xeriscaping). For example in California, the East Bay Municipal Utility District sponsored the publication of a book on plants and landscapes for summer-dry climates. Cacti are one group of drought-resistant plants recommended for dry landscape gardening.</p>

