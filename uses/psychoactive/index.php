                <p>Lophophora williamsii is native to northern Mexico and southern Texas. Individual stems are about 2–6 cm (0.8–2.4 in) high with a diameter of 4–11 cm (1.6–4.3 in) and may be found in clumps up to 1 m (3 ft) wide. A large part of the stem is usually below ground. Mescaline is concentrated in the photosynthetic portion of the stem that is above ground. The center of the stem, which contains the growing point (the apical meristem), is sunken. Experienced collectors of peyote remove a thin slice from the top of the plant leaving the growing point intact, thus allowing the plant to regenerate. There is evidence that peyote was in use more than 5,500 years ago: dried peyote buttons presumed to be from a site on the Rio Grande, Texas, were radiocarbon dated to around 3780–3660 BC. Peyote is perceived as a means of accessing the spirit world. Attempts by the Roman Catholic church to suppress its use after the Spanish conquest were largely unsuccessful and by the middle of the twentieth century peyote was more widely used than ever by indigenous peoples as far north as Canada. It is now used formally by the Native American Church.</p>

