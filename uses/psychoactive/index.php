                <p>Echinopsis pachanoi is native to Ecuador and Peru. It is very different in appearance to L. williamsii. It has tall stems, up to 6 m (20 ft) high with a diameter of 6–15 cm (2.4–5.9 in), which branch from the base, giving the whole plant a shrubby or treelike appearance. Archaeological evidence of the use of this cactus appears to date back to 2,000–2,300 years ago, with carvings and ceramic objects showing columnar cacti. Although church authorities under the Spanish attempted to suppress its use, this failed, as shown by the Christian element in the common name "San Pedro cactus"—Saint Peter cactus. Anderson attributes the name to the belief that just as St Peter holds the keys to heaven, the effects of the cactus allow users "to reach heaven while still on earth." It continues to be used for its psychoactive effects, both for spiritual purposes and for healing purposes, often combined with other psychoactive agents, such as Datura ferox and tobacco. Several other species of Echinopsis, including E. peruviana, also contain mescaline.</p>

