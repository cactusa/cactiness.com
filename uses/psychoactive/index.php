            	<h1>Uses for cacti and succulents</h1>
                <h2>Psychoactive use</h2>
                
                
                <p>A number of species of cacti have been shown to contain psychoactive agents, chemical compounds that can cause changes in mood, perception and cognition through their effects on the brain. Two species have a long history of use by the indigenous peoples of the Americas: peyote, Lophophora williamsii, in North America, and the San Pedro cactus, Echinopsis pachanoi, in South America. Both contain mescaline.</p>


